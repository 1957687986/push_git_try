num_str = "0123456789"

# 截取2-5之间的字符串
print(num_str[2:6])

# 截取2到末尾的字符串
print(num_str[2:])

# 截取开始到5的字符串
print(num_str[:6])

# 截取完整字符串
print(num_str[:])

# 从开始每隔一个字符截取字符串
print(num_str[::2])

# 从1开始每隔一个字符截取字符串
print(num_str[1::2])

# 截取从2开始到（末尾-1）的字符串
print(num_str[2:-1])

# 截取字符串末尾两个字符
print(num_str[-2:])

# 字符串逆序
print(num_str[-1::-1])

print(num_str)